<?php
class PackageCategory extends BaseSql
{
    protected $id;
    protected $name;

    //getters and setters
    public function getId()
    {
        return $this->id;
    }

    public function setId($id): void
    {
        $this->id = $id;
    }

    public function getName()
    {
        return $this->name;
    }

    public function setName($name): void
    {
        $this->name = $name;
    }
}